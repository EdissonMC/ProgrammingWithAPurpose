public class MathVarios {
    public static void main(String[] args) {
        double a = 30;

        // converting values to radians
        double b = Math.toRadians(a);

        System.out.println(Math.sin(b));

        a = 45;

        // converting values to radians
        b = Math.toRadians(a);

        System.out.println(Math.sin(b));

        a = 60;

        // converting values to radians
        b = Math.toRadians(a);

        System.out.println(Math.sin(b));

        a = 90;

        // converting values to radians
        b = Math.toRadians(a);

        System.out.println(Math.sin(b));

        int c = 3;
        System.out.println(Math.sqrt(c)); // Raiz Cuadrada

        System.out.println(Math.pow(c, 2)); //  elevado al cuadrado

    }
}
